<?php 
// Perulangan
// for
// while
// do..while
// foreach : Perulangan khusus array

// for
for ($i=0; $i < 5; $i++) { 
	echo "Hello World! <br>";
}
echo "<br>";

// while
$i = 0;
while ($i < 5) {
	echo "Hallo Dunia! <br>";
	$i++;
}
echo "<br>";

$i = 0;
// do..while
do {
	echo "Haii World! <br>";
	$i++;
} while ($i < 5);
echo "<br>";

 ?>